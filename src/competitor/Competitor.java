package competitor;

public class Competitor {
	
	// attribute
	private String name;
	private int score;
	
	
	// methods
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getScore() {
		return score;
	}
	
	public void setScore(int score) {
		this.score = score;
	}
	
	
}
